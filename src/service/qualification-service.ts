/**
 * Fetches a qualification by its ID.
 * @param qualificationId - The ID of the qualification to fetch
 * @returns The qualification information if found, or null if not found
 */
export const getQualificationById = cache(
  async (qualificationId: QualificationId): Promise<QualificationInfo | null> => {
    console.info(`Fetching qualification with id ${qualificationId}`);
    const res = await pool.query(
      `
        SELECT
          "id",
          "eventId",
          "teamId",
          "name",
          "errorMessage"
        FROM qualification
        WHERE id = $1
     `,
      [qualificationId]
    );
    if (res.rowCount === 0) {
      return null;
    }
    return rowToQualification(res.rows[0]);
  }
);

/**
 * Fetches a list of event qualifications assigned to a specific user.
 * @param userId - The ID of the user to fetch qualifications for
 * @param eventId - The ID of the event to filter qualifications by
 * @returns A list of qualifications assigned to the specified user
 */
export const getQualificationsForUser = cache(
  async (userId: UserId, eventId: EventId): Promise<QualificationInfo[]> => {
    console.info(`Fetching qualifications for user ${userId}`);
    const res = await pool.query(
      `
        SELECT
          q."id",
          q."eventId",
          q."teamId",
          q."name",
          q."errorMessage"
        FROM qualification q
        JOIN "user_qualification" uq ON q.id = uq."qualificationId"
        WHERE uq."userId" = $1 AND q."eventId" = $2
     `,
      [userId, eventId]
    );
    return res.rows.map(rowToQualification);
  }
);

/**
 * Creates a new qualification in the database.
 * @param qual - The qualification information to create, excluding the ID which will be generated by the database
 * @param client - Optional database client to use for the transaction
 * @returns The created qualification information, including the generated ID
 */
export const createQualification = async (
  qual: Omit<QualificationInfo, 'id'>,
  client?: PoolClient
): Promise<QualificationInfo> => {
  console.info('Creating qualification with data:', qual);
  const db = client || pool;
  const res = await db.query(
    `
      INSERT INTO qualification (
        "eventId",
        "teamId",
        "name",
        "errorMessage"
      )
      VALUES ($1, $2, $3, $4)
      RETURNING
        "id",
        "eventId",
        "teamId",
        "name",
        "errorMessage"
    `,
    [qual.eventId, qual.teamId, qual.name, qual.errorMessage]
  );
  return rowToQualification(res.rows[0]);
};

/**
 * Updates an existing qualification in the database.
 * @param qualification - The updated qualification information
 * @param client - Optional database client to use for the transaction
 * @returns The updated qualification information after saving to the database
 */
export const updateQualification = async (
  qualification: QualificationInfo,
  client?: PoolClient
): Promise<QualificationInfo> => {
  console.info(`Updating qualification ${qualification.id} with data:`, qualification);
  const db = client || pool;
  const res = await db.query(
    `
      UPDATE qualification
      SET
        "eventId" = $1,
        "teamId" = $2,
        "name" = $3,
        "errorMessage" = $4
      WHERE id = $5
      RETURNING
        "id",
        "eventId",
        "teamId",
        "name",
        "errorMessage"
    `,
    [
      qualification.eventId,
      qualification.teamId,
      qualification.name,
      qualification.errorMessage,
      qualification.id
    ]
  );
  return rowToQualification(res.rows[0]);
};

