import pool from '@/db';
import { PoolClient } from 'pg';
import { cache } from 'react';

const rowToQualification = (row: any): QualificationInfo => ({
  id: row.id,
  eventId: row.eventId,
  teamId: row.teamId,
  name: row.name,
  errorMessage: row.errorMessage
});

/**
 * Fetches a list of qualifications associated with a specific event.
 * @param eventId - The ID of the event to fetch qualifications for
 * @returns A list of qualifications associated with the specified event
 */
export const getQualificationsForEvent = cache(
  async (eventId: EventId): Promise<QualificationInfo[]> => {
    console.info(`Fetching qualifications for event ${eventId}`);
    const res = await pool.query(
      `
        SELECT
          "id",
          "eventId",
          "teamId",
          "name",
          "errorMessage"
        FROM qualification
        WHERE "eventId" = $1
     `,
      [eventId]
    );
    return res.rows.map(rowToQualification);
  }
);

/**
 * Fetches a list of qualifications associated with specific events.
 * @param eventIds - List of EventIds to fetch qualifications for
 * @returns A list of qualifications associated with the specified events
 */
export const getQualificationsForEvents = cache(
  async (eventIds: EventId[]): Promise<QualificationInfo[]> => {
    console.info(`Fetching qualifications for events ${eventIds.toString()}`);
    if (eventIds.length === 0) {
      return [];
    }
    const res = await pool.query(
      `
        SELECT
          "id",
          "eventId",
          "teamId",
          "name",
          "errorMessage"
        FROM qualification
        WHERE "eventId" = ANY($1)
     `,
      [eventIds]
    );
    return res.rows.map(rowToQualification);
  }
);

